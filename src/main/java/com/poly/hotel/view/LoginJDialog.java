    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        txtUsername.setText(placeholderUser);
        txtPassword.setEchoChar((char) 0);
        txtPassword.setForeground(Color.GRAY);
        txtPassword.setText(placeholderPass);
    }

    @Override
    public void login() {
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        UserDAO dao = new UserDAOImpl();
        User user = dao.findById(username);
        if (username.isEmpty()) {
            MsgBox.alert("Tên đăng nhập không được để trống");
        } else if (username.length() > 40) {
            MsgBox.alert("Đạt quá giới hạn kí tự nhập vào");
        } else if (user == null) {
            MsgBox.alert("Tài khoản không tồn tại");
        } else if (password.isEmpty()) {
            MsgBox.alert("Vui lòng nhập mật khẩu");
        } else if (!password.equals(user.getPassword())) {
            MsgBox.alert("Sai mật khẩu đăng nhập!");
        } else if (!user.isActive()) {
            MsgBox.alert("Tài khoản của bạn đang tạm dừng!");
        } else {
            MsgBox.alertSuccess("Đăng nhập thành công");
            XAuth.user = user; // duy trì user đăng nhập
            this.dispose();
        }
    }


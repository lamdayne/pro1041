    @Override
    public void open() {
        this.setLocationRelativeTo(null);
        this.fillToTable();
    }
    
    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblCustomer.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getCustomerID(),
                item.getFullName(),
                item.isGender() ? "Nam" : "Nữ",
                item.getPhoneNumber(),
                item.getEmail(),
                item.getIdNumber(),
                item.getAddress(),
                false
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void checkAll() {
        this.setCheckedAll(true);
    }

    @Override
    public void unCheckAll() {
        this.setCheckedAll(false);
    }

    @Override
    public void deleteCheckedItems() {
        if (MsgBox.comfirm("Bạn thực sự muốn xóa các mục chọn?")) {
            for (int i = 0; i < tblCustomer.getRowCount(); i++) {
                if ((Boolean) tblCustomer.getValueAt(i, 7)) {
                    dao.deleteById(String.valueOf(items.get(i).getCustomerID()));
                }
            }
            this.fillToTable();
        }
    }
    
    private void setCheckedAll(boolean checked) {
        for (int i = 0; i < tblCustomer.getRowCount(); i++) {
            tblCustomer.setValueAt(checked, i, 7);
        }
    }

    
